//FOR LOOP

function FORLOOP() {
    var i = 12;
    for (i = 0; i < 5; i++) {
        alert(i);
    }
}


//WHILE LOOP

function WHILELoop() {
    var n = 0;
    while (n < 5) {
        alert(n);
        n++;
    }
}

//DO WHILE LOOP

function DOWHILELoop() {
    var i = 8;
    do {
        alert(i);
        i++;
    } while (i < 5);
}


